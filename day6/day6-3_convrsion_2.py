print("To convert feet into meters:")

feet = float(input("Enter the value in feet: "))
# 1 feet = 0.3048 m

conversion = feet * 0.3048
print("The value in m is:", conversion)

print("To convert meters into feet:")

m = float(input("Enter the value in meters(m): "))

conversion = m / 0.3048
print("The value in feet is:", conversion)
