print("To convert inches into centimeters:")

inches = float(input("Enter the value in inches: "))
# 1 inch = 2.5 cm

conversion = inches * 2.54
print("The value in cm is:", conversion)

print("To convert centimeters into inches:")

cm = float(input("Enter the value in centimeters(cm): "))
# 1 inch = 2.5 cm

conversion = cm / 2.54
print("The value in inches is:", conversion)
